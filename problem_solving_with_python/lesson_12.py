# Define the price of the item

price = 9

# Check if price is exactly 10
if price == 10:
    print("We will buy it")

# Check if price is not equal to 10
if price != 10:
    print("The price is not 10")

# Check if price is greater than 10
if price > 10:
    print("It is expensive")

# Check if price is less than or equal to 10
if price <= 10:
    print("It is cheap")