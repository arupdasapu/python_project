# List of numbers
number_tuple = [1, 2, 3]

# Add all elements by indexing directly
add_num_tuple = number_tuple[0] + number_tuple[1] + number_tuple[2]
print(add_num_tuple)

# Assign elements to variables
x = number_tuple[0]
y = number_tuple[1]
z = number_tuple[2]
print(x + y + z)

# Unpack list into variables
a, b, c = number_tuple
print(a + b + c)
