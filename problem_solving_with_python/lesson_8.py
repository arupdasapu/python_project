# Basic math operations
print(5-3)    # Subtraction
print(5+3)    # Addition
print(5*3)    # Multiplication
print(5/3)    # Division (decimal)
print(5//3)   # Floor division
print(5%3)    # Remainder
print(5**3)   # Exponentiation

# Update variable x
x = 5
x = x + 5     # Add 5
x += 5        # Add 5 again
print(x)

# Update variable y
y = 5
y -= 2        # Subtract 2
print(y)

# Order of operations
z = 5 + 3 * 2 ** 2
print(z)

p = (5+3) * 2 ** 2
print(p)

q = (1+2) * (5-3)
print(q)